use std::thread::{self, JoinHandle};

use anyhow::Context;

pub fn thread<F, T>(name: &str, f: F) -> JoinHandle<T>
where
    F: FnOnce() -> T + Send + 'static,
    T: Send + 'static,
{
    thread::Builder::new()
        .name(name.to_string())
        .spawn(f)
        .with_context(|| format!("starting thread: {name}"))
        .expect("failed to start the thread")
}
